"""
Intrudução á blocos de código
if/ elif      / else
se/ se não se / se não 
"""
entrada = input('Você deseja entra ou sair? ')

if entrada == 'entrar':
    print('Você entrou no sistema!')
elif entrada == 'sair':
    print('Você saiu...')
else:
    print('Você não digitou nem entrar e nem sair!')
